package zh.codegym.task.task04.task0437;


/* 
由 8 组成的三角形
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < i + 1; j++) {
                System.out.printf("8");
            }
            System.out.println();
        }

    }
}
